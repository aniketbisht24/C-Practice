#include<bits/stdc++.h>
using namespace std;

int main()
{	
	int a = 5;
	a = a+1;

	// ++a first increments a then use it
	// a++ first use the variable and then increment it

	cout << "a " << a << endl;

	cout << "a++ " << a++ << endl;

	cout << "a " << a << endl;

	cout << "++a " << ++a << endl;

	cout << "a " << a << endl;

	cout << "a-- "<< a-- << endl;

	cout << "a "<<a << endl;

	cout << "--a"<< --a << endl;

}
